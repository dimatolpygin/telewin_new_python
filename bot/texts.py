# -*- coding: utf-8 -*-
"""Тексты бота, общие для ВСЕХ каналов (Telegram/VK/MAX).

Единый источник, чтобы приветствие и служебные ответы не расходились между
каналами (этап 25). Каждый адаптер импортирует отсюда, а не хранит свою копию.
"""

WELCOME = (
    "Здравствуйте! Я помогу найти товар в магазине «Домашний мастер» — подскажу цену и наличие.\n\n"
    "Спрашивайте обычными словами, например:\n"
    "• нужны гвозди сотка\n"
    "• саморезы для гипсокартона\n"
    "• чем прикрутить профлист\n\n"
    "Команда /reset — начать диалог заново."
)

# Ответ на сброс диалога (/reset и его эквиваленты в каналах).
RESET_OK = "Диалог сброшен. Что ищем?"

# Ответ при внутренней ошибке обработки (связь/таймаут).
ERROR_RETRY = "Секунду, связь подвисла — повторите запрос, пожалуйста."


def kontakt(phone: str) -> str:
    """Ответ на кнопку связи (этап 35). Номер приходит из `.env` (SHOP_PHONE),
    в коде не хардкодится. Пусто в конфиге → честно говорим, что связи нет,
    вместо пустой строки с двоеточием."""
    if not phone:
        return "Телефон магазина сейчас недоступен, напишите вопрос сюда, помогу подобрать."
    return (f"Звоните: {phone}. Ответим в рабочее время, подскажем по наличию "
            "и поможем подобрать. Можно и здесь спросить, я на связи.")
