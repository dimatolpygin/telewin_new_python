# Маркеры ОТКАЗА в ответе модели: и «товара нет в прайсе», и категорийное «не наш
# ассортимент». Оба на первом ходу без поиска — подозрительны: модель может неверно
# отнести реальный товар прайса к непрофильному (баг «масло пихтовое» — модель сочла его
# косметикой и отказала, не заглянув в прайс). Форсируем поиск в обоих случаях.
_RE_OTKAZ = re.compile(
    r"не найд|не нашл|нет в (?:прайсе|каталоге|наличии|ассортимент)|отсутству|не значит|"
    r"не удалось найти|не (?:наш|нашего)|не прода[её]м|не торгуем|не занимаемся|"
    r"не вход(?:ит|ят) в ассортимент|в ассортимент[еа]? (?:нет|отсутству)",
    re.I,
)


def _zayavil_otkaz(text: str) -> bool:
    """Ответ — отказ (нет товара / не наш профиль), а не показ товара или уточнение."""
    return bool(_RE_OTKAZ.search(text))


# Светская реплика покупателя (приветствие, благодарность, команда) — товара в ней нет,
# поиск по ней форсировать бессмысленно: гард для предохранителя уточнений ниже.
_RE_BOLTOVNYA = re.compile(
    r"^\W*(привет|здравств|добрый|доброе|хай|ку\b|салют|спасибо|благодар|пока\b|до свидан|"
    r"ок\b|окей|хорошо|ага|да\b|нет\b|как дела|кто ты|ты бот|/start|/reset|/help)",
    re.I,
)


def _boltovnya(text: str) -> bool:
    """Реплика без товара — «привет», «спасибо», команда бота."""
    t = text.strip()
    return len(t) < 40 and bool(_RE_BOLTOVNYA.match(t))


def _zadal_vopros(text: str) -> bool:
    """Ответ — уточняющий вопрос покупателю, а не показ товара."""
    return "?" in text

