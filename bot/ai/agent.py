@dataclass
class AgentResult:
    answer: str
    new_history: list[dict]
    zaprosy_poiska: list[str] = field(default_factory=list)
    naydeno: int = 0


async def run_agent(
    cfg: OpenRouterConfig, poisk: Poisk, history: list[dict], user_text: str,
    use_podgr: bool = True, gibrid=None, data_prajsa: str = ДАТА_ПРАЙСА,
) -> AgentResult:
    """Если передан `gibrid` (этап 10) — поиск идёт через гибрид (лексика ⊕ вектор, RRF);
    иначе через чистую лексику `poisk.iskat`. Это позволяет запускать бота без БД/эмбеддингов.
    `data_prajsa` (этап 20) — дата актуальности прайса (dd.mm.yyyy) из `price_meta`; уходит
    в промт и в payload инструмента, чтобы бот не выдавал вчерашнее за свежее."""
    system = SYSTEM.replace("{ДАТА}", data_prajsa)
    messages: list[dict] = [{"role": "system", "content": system}, *history,
                            {"role": "user", "content": user_text}]
    zaprosy_poiska: list[str] = []
    poslednee_naydeno = 0
    forsirovano = False   # предохранитель форсировали не более одного раза за ход
    forsim_sejchas = False

    for _ in range(4):
        res = await chat(cfg, messages, TOOLS,
                         tool_choice="required" if forsim_sejchas else "auto")
        forsim_sejchas = False

        if res.get("tool_calls"):
            messages.append({"role": "assistant", "content": res.get("content") or "",
                             "tool_calls": res["tool_calls"]})
            for tc in res["tool_calls"]:
                assort = False
                try:
                    args = json.loads(tc["function"]["arguments"])
                    query = args.get("query", "")
                    assort = bool(args.get("assortiment", False))
                except (json.JSONDecodeError, KeyError, TypeError):
                    query = ""
                zaprosy_poiska.append(query)
                # адаптивный лимит: точечный запрос — до 8, «какие есть/весь выбор» — до 15
                top = 15 if assort else 8
                if gibrid is not None:
                    rezultaty, kanal = await gibrid.iskat(query, top=top, use_podgr=use_podgr)
                else:
                    rezultaty, kanal = poisk.iskat(query, top=top, use_podgr=use_podgr)
                poslednee_naydeno = len(rezultaty)
                log_tool_call("search_products",
                              {"query": query, "assortiment": assort, "канал": kanal},
                              poslednee_naydeno)
                # честная пометка бренда (этап 15): покупатель назвал бренд, а ни одна
                # найденная позиция ему не соответствует → это аналоги, не искомый бренд.
                brend_disp, brend_match = poisk.brend_iz_zaprosa(query)
                brend_est = brend_disp and any(
                    (r.get("proizvoditel") or "").lower() in brend_match for r in rezultaty)
                payload = {
                    "дата_актуальности": data_prajsa,
                    "канал_поиска": kanal,
                    "найдено": [{
                        "наименование": r.get("imya"),
                        "цена": r.get("cena"),
                        "единица": r.get("edinica"),
                        "остаток_всего": r.get("ostatok_obshiy"),
                        "остаток_микрорайон": r.get("ostatok_mikro"),
                        "остаток_березовская": r.get("ostatok_berez"),
                        "артикул": r.get("artikul"),
                        # этап 34: категории прайса — чтобы уточняющий вопрос строился по
                        # РЕАЛЬНОМУ ассортименту, а не по общим знаниям модели о мире
                        # («радиатор» → биметалл/чугун, а не «автомобильный?»).
                        "группа": r.get("gruppa"),
                        "подгруппа": r.get("podgruppa"),
                    } for r in rezultaty],
                }
                if brend_disp and not brend_est:
                    payload["примечание_бренд"] = (
                        f"Товаров бренда «{brend_disp}» по этому запросу в наличии нет. "
                        "Показаны аналоги других производителей."
                    )
                messages.append({"role": "tool", "tool_call_id": tc["id"],
                                 "content": json.dumps(payload, ensure_ascii=False)})
            continue  # даём модели обработать результат

        answer = (res.get("content") or "").strip()
        # Модель изредка отдаёт пустой content без tool_calls (наблюдалось на haiku после
        # результата поиска). Заглушка «Не понял запрос» здесь была бы враньём — данные уже
        # есть, просто ответ не сгенерировался. Даём ещё одну итерацию цикла.
        if not answer:
            logger.warning("Пустой ответ модели — повторяю итерацию")
            messages.append({"role": "system", "content":
                             "Ответь покупателю текстом по уже полученным данным."})
            continue

        # ПРЕДОХРАНИТЕЛЬ: модель ОТКАЗАЛА (нет товара / не наш профиль), ни разу не вызвав
        # поиск в этом ходу → возможна галлюцинация (баг «масло пихтовое»: модель сочла его
        # косметикой и отказала, не заглянув в прайс). Не отдаём такой ответ — форсируем
        # реальный поиск (tool_choice=required) и переотвечаем. Уточняющие вопросы под
        # маркеры отказа не попадают, поэтому они проходят как есть.
        if not zaprosy_poiska and not forsirovano and _zayavil_otkaz(answer):
            forsirovano = True
            forsim_sejchas = True
            logger.warning("Агент отказал без поиска — форсирую search_products")
            messages.append({"role": "system", "content":
                             "Ты не проверил прайс. Обязательно вызови search_products по товару "
                             "из запроса покупателя, прежде чем делать вывод о наличии — многие "
                             "пограничные товары (масла, химия, садовое) у нас есть."})
            continue

        # ПРЕДОХРАНИТЕЛЬ (этап 34): модель задала уточняющий вопрос, ни разу не заглянув в прайс
        # → варианты в вопросе взяты из общих знаний о мире, а не из нашего ассортимента («радиатор
        # автомобильный?», «труба дымоходная?»). Промтом это не гарантируется — форсируем поиск и
        # переспрашиваем уже по реальной выдаче. Светская реплика («привет») сюда не попадает.
        if (not zaprosy_poiska and not forsirovano and _zadal_vopros(answer)
                and not _boltovnya(user_text)):
            forsirovano = True
            forsim_sejchas = True
            logger.warning("Агент уточняет без поиска — форсирую search_products")
            messages.append({"role": "system", "content":
                             "Не спрашивай покупателя, не заглянув в прайс. Вызови search_products "
                             "по товару из его запроса (assortiment=true) и уточняй ТОЛЬКО по тем "
                             "видам, которые реально есть в выдаче."})
            continue

        # Постобработка стиля (этап 32): снять «—» и markdown, которые модель иногда
        # проставляет вопреки промту. Чистим и то, что уйдёт покупателю, и то, что ляжет
        # в историю (иначе модель копирует свой прежний формат в следующих ответах).
        answer = ochistit_otvet(answer)
        new_history = [*history, {"role": "user", "content": user_text},
                       {"role": "assistant", "content": answer}]
        return AgentResult(answer, new_history, zaprosy_poiska, poslednee_naydeno)

    logger.warning("Агент исчерпал лимит итераций")
    answer = "Уточните запрос, пожалуйста — не смог подобрать товар."
    return AgentResult(
        answer,
        [*history, {"role": "user", "content": user_text}, {"role": "assistant", "content": answer}],
        zaprosy_poiska, poslednee_naydeno,
    )
