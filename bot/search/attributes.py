# -*- coding: utf-8 -*-
"""Детерминированный разбор названия из прайса в атрибуты.
Регулярки, без ИИ. Всё, что вытаскивается правилами, ИИ трогать не должен.
Порт atributy.py (один-в-один).
"""
import re

_NUM = r"\d+(?:[.,]\d+)?"


def _f(s: str) -> float:
    return float(s.replace(",", "."))


def razobrat(name: str) -> dict:
    """Название -> {резьба_m, диаметр_мм, длина_мм, мощность_вт, цоколь, дюймы, зернистость}"""
    a: dict = {}
    n = " " + name.strip() + " "

    # Метрическая резьба с длиной: "Болт M 5*12", "ШСГМ M4*20"
    m = re.search(rf"[MМ]\s*({_NUM})\s*[*xх×]\s*({_NUM})", n, re.I)
    if m:
        a["резьба_m"] = _f(m.group(1))
        a["длина_мм"] = _f(m.group(2))
    else:
        # Резьба без длины: "Гайка M10", "Шайба M 3"
        m = re.search(rf"[MМ]\s*({_NUM})(?![\d/*xх×])", n, re.I)
        if m:
            a["резьба_m"] = _f(m.group(1))

    # Длина в метрах/сантиметрах (этап 9): "3м", "5 метров", "175 см", "200см".
    # Головной размер изделия (рулетка «3м*16мм», шланг «175 см») приоритетнее
    # ширины в мм — поэтому ставим ДО разбора «*»/мм и нормируем в мм.
    # Единица «м/метр» ↔ *1000, «см/сантиметр» ↔ *10. «мм» отсекаем lookahead'ом.
    # Не хватаем число ПОСЛЕ «*»/«х» (это ширина пары «45*160см» → пусть идёт в диаметр*длина);
    # у диапазона «50/60 м» берём ПЕРВОЕ число (как L-паттерн: «L 50/60 м» → 50).
    if "длина_мм" not in a:
        m = re.search(rf"(?<![\d.,*xхХ×])({_NUM})(?:\s*[/\-–]\s*{_NUM})?\s*(метр\w*|м|сантиметр\w*|см)(?![а-я])", n, re.I)
        if m:
            a["длина_мм"] = _f(m.group(1)) * (1000 if m.group(2).lower()[0] == "м" else 10)

    # Диаметр*длина: "ШСГД 3,5*45", "Гвоздь строительный 4*100", "Лента Р 40 75*457"
    if "длина_мм" not in a:
        m = re.search(rf"(?<![MМ])\b({_NUM})\s*[*xх×]\s*({_NUM})", n, re.I)
        if m:
            a["диаметр_мм"] = _f(m.group(1))
            a["длина_мм"] = _f(m.group(2))

    # Размер через слэш: "Дюбель RD 6/36", "Дюбель 4 BS-K 10/66".
    # Отличаем от дюймов: у дюймов знаменатель 2/4/8 и числитель меньше его.
    if "длина_мм" not in a:
        m = re.search(r"\b(\d{1,2})\s?/\s?(\d{1,3})\b", n)
        if m:
            ch, zn = int(m.group(1)), int(m.group(2))
            if not (zn in (2, 4, 8) and ch < zn):
                a["диаметр_мм"] = float(ch)
                a["длина_мм"] = float(zn)

    # Зернистость абразива: "Р 40", "P 80" (шкурка, лента, круг)
    m = re.search(r"\b[РP]\s?(\d{2,4})\b", n)
    if m:
        a["зернистость"] = int(m.group(1))

    # Мощность: "60Вт", "40 Вт"
    m = re.search(rf"({_NUM})\s*Вт", n, re.I)
    if m:
        a["мощность_вт"] = _f(m.group(1))

    # Ток автомата (этап 14): "16А", "25 А", "40А". Цифра перед А (не путать с «ВА
    # 47-29» — там А без цифры). Кир/лат А через re.I. Автоматы/УЗО/дифавтоматы.
    m = re.search(r"(\d{1,3})\s*А\b", n, re.I)
    if m:
        a["ток_а"] = int(m.group(1))

    # Вес (этап 6): "25кг", "2 кг", "1,5 кг", "500 г", "500 грамм". Нормируем в граммы.
    # Единица обязательна (якорь) — голое число весом не считаем. 'кг' проверяем раньше 'г'.
    m = re.search(rf"({_NUM})\s*(?:кг|килограмм\w*)\b", n, re.I)
    if m:
        a["вес_г"] = _f(m.group(1)) * 1000
    else:
        m = re.search(rf"({_NUM})\s*(?:гр?|грамм\w*)\b", n, re.I)
        if m:
            a["вес_г"] = _f(m.group(1))

    # Цоколь лампы (этап 14): кириллическая Е и латинская E + штырьковые/точечные
    # G-цоколи (GU10/GX53/G9/G4/…). Нормируем в единый канон (Е→E, без пробелов,
    # запятая→точка): E27, GU10, GU5.3, GX53, G9, T8, R7S — как в sleng['цоколь'].
    m = re.search(r"\b([ЕE]\s?(?:14|27|40)|GU\s?10|GU\s?5[.,]3|GX\s?53|GX\s?70|G23|G13|G9|G4|T8|T5|R7S)\b", n, re.I)
    if m:
        a["цоколь"] = m.group(1).upper().replace(" ", "").replace(",", ".").replace("Е", "E")

    # Цветовая температура (этап 14): "2700К", "4000K", "6500 К". Только с якорем К
    # (голое 4-значное число температурой не считаем). Диапазон 2000–6900.
    m = re.search(r"\b([2-6]\d00)\s*[КK]\b", n)
    if m:
        a["цв_температура"] = int(m.group(1))

    # Дюймы сантехники (этап 14): различаем 1/2 (15мм) от полутора дюймов «1 1/2»
    # (40мм). Прайс пишет полтора как «1 1/2"» или «1" 1/2 (40)», НЕ символом ½.
    # Целое ПЕРЕД дробью → отдельный размер («1 1/2» ≠ «1/2»), иначе простая дробь.
    m = re.search(r"\b(\d)\s*[\"'’]?\s*1\s*/\s*2\b", n)
    if m:
        a["дюймы"] = f"{m.group(1)} 1/2"
    else:
        m = re.search(r"\b(\d)\s*[\"'’]?\s*1\s*/\s*4\b", n)
        if m:
            a["дюймы"] = f"{m.group(1)} 1/4"
        else:
            # простая дробь: "1/2", "3/4". Знаменатель только 2/4/8, иначе это размер.
            m = re.search(r"\b([1-7])\s?/\s?([248])\b", n)
            if m and int(m.group(1)) < int(m.group(2)):
                a["дюймы"] = f"{m.group(1)}/{m.group(2)}"

    # Длина инструмента: "L 300", "600мм"
    if "длина_мм" not in a:
        m = re.search(rf"\bL\s*({_NUM})", n)
        if not m:
            m = re.search(rf"({_NUM})\s*мм\b", n, re.I)
        if m:
            a["длина_мм"] = _f(m.group(1))

    return a
