# -*- coding: utf-8 -*-
"""Обогащение имени товара для эмбеддинга (этап 10).

Прайс пишет имена телеграфно: «Сверло по дер. перов.», «Болт M 5*12». Голый вектор
такого имени плохо ловит родовой запрос («перовое сверло по дереву»). Обогащаем:
  1) раскрываем безопасные материал/свойство-сокращения («дер.»→«дерево», «оцинк.»→
     «оцинкованный») — только однозначные, где раскрытие не поменяет смысл;
  2) дописываем таксономию (семейство/подгруппа/группа) — она даёт вектору контекст
     категории, за счёт чего «болгарка» тянется к «УШМ», а «крепёж» — к болтам/саморезам.
"""
import re

# Только однозначные материал/свойство-сокращения. Спорные («гн.», «кл.», «гр.»,
# «шт.», «м.») НЕ раскрываем — раскрытие исказило бы смысл.
_СОКРАЩЕНИЯ = {
    "дер": "дерево",
    "мет": "металл",
    "метал": "металл",
    "металл": "металл",
    "бет": "бетон",
    "оцинк": "оцинкованный",
    "нерж": "нержавеющий",
    "пластик": "пластиковый",
    "пласт": "пластиковый",
    "пластм": "пластмассовый",
    "бел": "белый",
    "чер": "чёрный",
    "черн": "чёрный",
    "выкл": "выключатель",
    "светодиод": "светодиодный",
    "светод": "светодиодный",
    "алмаз": "алмазный",
    "алюмин": "алюминиевый",
    "алюм": "алюминиевый",
    "силик": "силиконовый",
    "аэрозол": "аэрозольный",
    "универсал": "универсальный",
    "комбинир": "комбинированный",
    "галоген": "галогенный",
    "наждач": "наждачный",
    "монт": "монтажный",
    "усил": "усиленный",
    "перех": "переходник",
    "бесконеч": "бесконечная",
    "перов": "перовое",
    "комп": "компьютерный",
    "проф": "профильный",
}

# Слово с точкой: буквы + точка. Раскрываем по нижнему регистру основы.
_СОКР_RE = re.compile(r"([А-Яа-яёЁ]+)\.")


def _раскрыть(m: re.Match) -> str:
    осн = m.group(1).lower()
    зам = _СОКРАЩЕНИЯ.get(осн)
    return зам if зам is not None else m.group(0)


def раскрыть_сокращения(имя: str) -> str:
    return _СОКР_RE.sub(_раскрыть, имя)


def обогатить(товар: dict) -> str:
    """Текст для эмбеддинга: раскрытое имя + таксономия (без дублей)."""
    имя = раскрыть_сокращения(товар.get("imya", ""))
    низ = имя.lower()
    части = [имя]
    for ключ in ("semeystvo", "podgruppa", "gruppa"):
        v = (товар.get(ключ) or "").strip()
        if v and v.lower() not in низ:
            части.append(v)
    return ", ".join(части)
